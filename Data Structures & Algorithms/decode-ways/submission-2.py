class Solution:
    def numDecodings(self, s: str) -> int:
        if not s or s[0] == "0":
            return 0
            
        n = len(s)
        # יצירת מערך בגודל n + 1 מאותחל באפסים
        dp = [0] * (n + 1)
        
        # מקרי בסיס
        dp[0] = 1  # מחרוזת ריקה - בסיס מתמטי
        dp[1] = 1  # האות הראשונה חוקית (כי בדקנו שהיא לא '0')
        
        # רצים מאינדקס 2 של מערך ה-dp (שמתאים לאינדקס 1 במחרוזת s)
        for i in range(2, n + 1):
            # התו הנוכחי בודד (הצצה לאינדקס i-1 בתוך s)
            one_digit = s[i - 1]
            # הצמד שמסתיים בתו הנוכחי (הצצה לשני התווים האחרונים ב-s)
            two_digits = s[i - 2 : i]
            
            # אפשרות 1: אם התו הבודד חוקי (לא '0'), הוא לוקח את כל האפשרויות שלפניו
            if one_digit != "0":
                dp[i] += dp[i - 1]
                
            # אפשרות 2: אם הצמד חוקי (בין "10" ל-"26"), הוא מוסיף את האפשרויות שלפני הצירוף
            if "10" <= two_digits <= "26":
                dp[i] += dp[i - 2]
                
            # אופטימיזציה: אם הגענו למצב שאי אפשר לפענח את התא הנוכחי באף דרך (למשל "30")
            if dp[i] == 0:
                return 0
                
        # התא האחרון במערך מכיל את סך כל הדרכים עבור המחרוזת המלאה
        return dp[n]


        
